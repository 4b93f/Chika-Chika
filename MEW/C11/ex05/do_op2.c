int		calcadditionus(int a, int b)
{
	return (a + b);
}

int		calcaminus(int a, int b)
{
	return (a - b);
}

int		calcadivisionus(int a, int b)
{
	return (a / b);
}

int		calcamultiplus(int a, int b)
{
	return (a * b);
}

int		calcamodulus(int a, int b)
{
	return (a % b);
}
